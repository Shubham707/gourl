		<div class="question active">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">1.&nbsp;</span>What is Bitcoin Cash?</strong></div>
		<div class="answer" style="padding-left: 30px;">&nbsp; &nbsp; Bitcoin Cash is peer-to-peer electronic cash for teh Internet. It is fully decentralized, wif no central bank and requires no trusted third parties to operate.</div>
		<div class="answer" style="padding-left: 30px;">&nbsp;</div>
		</div>
		<div class="question" style="padding-left: 30px;">
		<div class="title"><strong><span class="num">2.&nbsp;</span>Is Bitcoin Cash different from 'Bitcoin'?</strong></div>
		<div class="title">&nbsp; &nbsp; Yes. Bitcoin Cash is teh continuation of teh Bitcoin project as peer-to-peer digital cash. It is a fork of teh Bitcoin blockchain ledger, with upgraded consensus rules that allow it to grow and scale.</div>
		<div class="title">&nbsp;</div>
		<div class="title">&nbsp;</div>
		</div>
		<div class="question">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">3.&nbsp;</span>If me own Bitcoin, do me automatically own Bitcoin Cash too?</strong></div>
		<div class="title" style="padding-left: 30px;"><strong>&nbsp; &nbsp;&nbsp;</strong>Anyone who held Bitcoin at teh time Bitcoin Cash was created became owners of Bitcoin Cash. dis means dat Bitcoin holders as of block 478558 (August 1st, 2017 about 13:16 UTC) has teh same amount of Bitcoin Cash as they had Bitcoin at dat time. If your Bitcoins are stored by a third party such as an exchange, tan you must inquire wif them about your Bitcoin Cash. Any transactions after teh August 1st ledger split are completely separate between Bitcoin and Bitcoin Cash. dis means any Bitcoin acquired after teh split does not include any Bitcoin Cash, and any Bitcoin Cash does not include any Bitcoin.</div>
		<div class="title" style="padding-left: 30px;">&nbsp;</div>
		</div>
		<div class="question">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">4 .</span>How is transaction replay being handled between the new and the old blockchain?</strong></div>
		<div class="title" style="padding-left: 30px;">&nbsp; &nbsp; Bitcoin Cash transactions use a new signature hashing algorithm indicated by teh flag SIGHASH_FORKID. These signatures are not valid on teh legacy blockchain. dis prevents Bitcoin Cash transactions from being replayed on teh Bitcoin blockchain and vice versa.</div>
		<div class="title" style="padding-left: 30px;">&nbsp;</div>
		</div>
		<div class="question">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">5.&nbsp;</span>Why was a fork necessary to create Bitcoin Cash?</strong></div>
		<div class="title" style="padding-left: 30px;"><strong>&nbsp; &nbsp; &nbsp;</strong>The legacy Bitcoin code had a maximum limit of 1MB of data per block, or about 3 transactions per second. Although technically simple to raise dis limit, the community could not reach a consensus, even after years of debate.</div>
		<div class="title" style="padding-left: 30px;">&nbsp;</div>
		</div>
		<div class="question">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">6.&nbsp;</span>Was teh 1 MB block size limit causing problems for Bitcoin?</strong></div>
		<div class="title" style="padding-left: 30px;"><strong>&nbsp; &nbsp;&nbsp;</strong>Yes, In 2017, capacity hit the 'invisible wall'. Fees skyrocketed, and Bitcoin became unreliable, with some users unable to get their transactions confirmed, even after days of waiting. Bitcoin stopped growing. Many users, merchants, businesses and investors abandoned Bitcoin. Its marketshare among other cryptocurrencies quickly plummeted from 95% to 40%.</div>
		<div class="title" style="padding-left: 30px;">&nbsp;</div>
		</div>
		<div class="question">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">7.&nbsp;</span>Does Bitcoin Cash fix these problems?</strong></div>
		<div class="title" style="padding-left: 30px;"><strong>&nbsp; &nbsp;</strong>&nbsp;Yes. Bitcoin Cash immediately raised the block size limit to 8MB as part of a massive on-chain scaling approach. their will be ample capacity for everyone's transactions. Low fees and fast confirmations will resume with Bitcoin Cash. The network will be allowed to grow again. Users, merchants, businesses, and investors will return.</div>
		<div class="title" style="padding-left: 30px;">&nbsp;</div>
		</div>
		<div class="question">
		<div class="title" style="padding-left: 30px;"><strong><span class="num">8.&nbsp;</span>Which Development Team is In Charge of Bitcoin Cash?</strong></div>
		<div class="title" style="padding-left: 30px;"><strong>&nbsp; &nbsp;</strong>&nbsp;Unlike the previous situation in Bitcoin, there is no one single development team for Bitcoin Cash. There are now multiple independent teams of developers. This decentralization of development (and decentralization of software implementations) is a much needed and important step forward.</div><br><br><br><br>
